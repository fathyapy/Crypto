CREATE DATABASE [BlueJack Coin Market]
GO
USE [BlueJack Coin Market]
GO
CREATE TABLE Exchange(
	ExchangeId CHAR(5) PRIMARY KEY,
	ExchangeName VARCHAR(100),
	ExchangeAddress VARCHAR(255)
)

CREATE TABLE Customer(
	CustomerId CHAR(5) PRIMARY KEY,
	CustomerName VARCHAR(255),
	CustomerAddress VARCHAR(255),
	CustomerGender VARCHAR(10),
	CustomerPhone VARCHAR(13)
)

CREATE TABLE Cryptocurrency(
	CryptoId CHAR(5) PRIMARY KEY,
	CryptoName VARCHAR(255)
)

CREATE TABLE Wallet(
	CustomerId CHAR(5) FOREIGN KEY REFERENCES Customer(CustomerId),
	CryptoId CHAR(5) FOREIGN KEY REFERENCES Cryptocurrency(CryptoId),
	WalletAddress VARCHAR(100),
	Balance FLOAT,
	PRIMARY KEY(CustomerId,CryptoId)
)

CREATE TABLE TransactionType(
	TransactionTypeId CHAR(5) PRIMARY KEY,
	TransactionTypeName VARCHAR(255)
)

CREATE TABLE HeaderTransaction(
	TransactionId CHAR(5) PRIMARY KEY,
	ExchangeId CHAR(5) FOREIGN KEY REFERENCES Exchange(ExchangeId),
	CustomerId CHAR(5) FOREIGN KEY REFERENCES Customer(CustomerId),
	CryptoId CHAR(5) FOREIGN KEY REFERENCES Cryptocurrency(CryptoId),
	TransactionTypeId CHAR(5) FOREIGN KEY REFERENCES TransactionType(TransactionTypeId),
	Amount FLOAT,
	Price FLOAT,
	TransactionDate DATE
)

